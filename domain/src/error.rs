/// Ошибка порта-репозитория. Домен описывает её абстрактно; конкретный адаптер
/// (`infrastructure`) маппит свои ошибки (sqlx, сеть) в эти варианты, не протекая
/// деталями наружу.
#[derive(Debug, thiserror::Error)]
pub enum RepositoryError {
    /// Запись не найдена там, где она ожидалась.
    #[error("запись не найдена")]
    NotFound,
    /// Нарушение уникальности/конфликт версий (например занятый handle).
    #[error("конфликт: запись уже существует или изменена")]
    Conflict,
    /// Хранилище недоступно (соединение, таймаут, внутренняя ошибка драйвера).
    #[error("хранилище недоступно: {0}")]
    Unavailable(String),
}
